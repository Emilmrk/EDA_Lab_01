public class Variables {
    public static void main(String[] args) {

        String Nombre="Luis";
        int numero_entero=6;
        float numero_decimal=6.647f;
        char caracter_simple='A' ;
        boolean verdad_falso=false;


        System.out.println("Este es un String: "+Nombre);
        System.out.println("Este es un int (numero entero): "+numero_entero);
        System.out.println("Este es un float (numero decimal): "+numero_decimal);
        System.out.println("Este es un char (caracter simple): "+caracter_simple);
        System.out.println("Este es un boolean (valor de verdad o falso): "+verdad_falso);


    }


}